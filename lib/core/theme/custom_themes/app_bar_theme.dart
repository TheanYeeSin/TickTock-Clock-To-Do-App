import "package:flutter/material.dart";

class TAppBarTheme {
  TAppBarTheme._();

  static AppBarTheme lightAppBarTheme() => const AppBarTheme(
        centerTitle: true,
        elevation: 0,
      );

  static AppBarTheme darkAppBarTheme() => const AppBarTheme(
        centerTitle: true,
        elevation: 0,
      );
}
